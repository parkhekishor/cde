package org.cde.domain.entity;

public class EntityAddress {
	private EntityAddressRaw addressRaw_;
	private EntityAddressStd addressStd_;
	
	public EntityAddressRaw getAddressRaw() {
		return addressRaw_;
	}
	public void setAddressRaw(EntityAddressRaw addressRaw) {
		addressRaw_ = addressRaw;
	}
	public EntityAddressStd getAddressStd() {
		return addressStd_;
	}
	public void setAddressStd(EntityAddressStd addressStd) {
		addressStd_ = addressStd;
	}
	
	
	

}
